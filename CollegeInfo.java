    private void searchFeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchFeeActionPerformed
        // TODO add your handling code here:
        if(feeTf.getText().isEmpty()){
            JOptionPane.showMessageDialog(rootPane, " Please enter the price value ! ");
        }
        DefaultTableModel table = (DefaultTableModel) demonstrationTable.getModel();
        
        int rows = demonstrationTable.getRowCount();
        
        int [] priceOfColleges = new int[rows];
        
         for(int i = 0 ; i< rows ; i++){
             
             priceOfColleges[i] = Integer.parseInt(demonstrationTable.getValueAt(i,5).toString());
         }
         
         int sortedPriceOfColleges [] = selectionSort(priceOfColleges);
         
         int userInput = Integer.parseInt(feeTf.getText());
         
         int searchIndex = binarySearch(sortedPriceOfColleges,userInput);
         
         if (searchIndex != -1){
             
             for(int i = 0; i<rows; i++){
                 if(Integer.parseInt(demonstrationTable.getValueAt(i,5).toString())== sortedPriceOfColleges[searchIndex]){
                     JOptionPane.showMessageDialog(rootPane, "The information about the college that was foundÂ is as follows: \nCollege Id : "+
                             table.getValueAt(i,0)+"\nCollege Name : "+ table.getValueAt(i, 1)+"\nCatagory : "+table.getValueAt(i,2)
                             +"\nCourse : "+table.getValueAt(i, 3) + "\nAffiliation : " + table.getValueAt(i,4) + "\nPrice : " + table.getValueAt(i,5) 
                             + "\nScholorship : " + table.getValueAt(i,6));
                      
                 }
             }
             
         }else
             JOptionPane.showMessageDialog(rootPane, "No college matched the input fee");
        
        
    }//GEN-LAST:event_searchFeeActionPerformed
    
    private void searchCollegeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchCollegeActionPerformed
        // TODO add your handling code here:
        
        DefaultTableModel table = (DefaultTableModel) demonstrationTable.getModel();
        String userInput = catagoryCb.getSelectedItem().toString();
        
        ArrayList <String> output = new ArrayList<>();
        
        int rows = demonstrationTable.getRowCount();
        
        
        for(int i = 0 ; i< rows ; i++){
            if (demonstrationTable.getValueAt(i, 2).toString().equals(userInput)){
                output.add(demonstrationTable.getValueAt(i, 1).toString());
            }
        }
        
        
        if(output.isEmpty()){
            JOptionPane.showMessageDialog(rootPane," No colleges found ! ");
        }else{
            String collegesName ="";
            int index = 0;
            
            do{
               collegesName = collegesName + output.get(index) + " , " ;
               index ++;
            }while(index<output.size());
            JOptionPane.showMessageDialog(rootPane,"There are "+ output.size()+ " colleges : " + collegesName);
        }
        
    }//GEN-LAST:event_searchCollegeActionPerformed

