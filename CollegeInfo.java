    private int [] selectionSort(int [] a ){
        for (int i = 0 ; i < a.length - 1; i++){
        int min = minimumPosition(a,i);
        swap(a,min,i);
       
        
    }
      return a;  
    }
    
    private int minimumPosition(int [] a , int from){
        int min = from;
        for (int i = from + 1  ; i < a.length ; i++){
            if(a[i] < a[min]){
                min = i;
                
            }
        }
        
        return min;
    }

    private void swap(int[] a, int i, int j) {
        int temp = a[i];
        a[i] = a[j];
        a[j] = temp;
      
    }
    
    
    
    public static int binarySearch(int [] a, int key){
        
       
         int low = 0;
         int high = a.length - 1 ;
         while(low <= high){
             int mid = (low + high )/ 2;
             
             if (a[mid]== key){
                 return mid;
             }
             else if( a[mid] <key){
                 low = mid + 1;
             }
             else{
                 high = mid -1;
             }
             
         }
          return -1;
    }
    
    
